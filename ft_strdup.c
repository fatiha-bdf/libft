#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (str[len])
		len++;
	dup = (char *)malloc(sizeof(*str) * len + 1);
	if (dup == NULL)
		return (NULL);
	while (i < len)
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
